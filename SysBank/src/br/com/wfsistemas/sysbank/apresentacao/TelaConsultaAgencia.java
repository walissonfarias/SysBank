    private void editarAgencia(){
        int linhaSelecionada = tblResultado.getSelectedRow();
        if(linhaSelecionada != -1){
            Agencia agenciaSelecionado = agencias.get(linhaSelecionada);            
            TelaEditarDadosAgencia telaEditarDadosAgencia = new TelaEditarDadosAgencia(this, agenciaSelecionado);          
            
            if(this.telaEditarDadosAgencia != null){
                this.telaEditarDadosAgencia.dispose();
            }
            this.telaEditarDadosAgencia = new TelaEditarDadosAgencia(this, agenciaSelecionado);
            this.telaEditarDadosAgencia.setVisible(true);
            this.telaEditarDadosAgencia.toFront();            
            
        }else{
            String mensagem = "Nenhum aluno selecionado.";
                JOptionPane.showMessageDialog(this,mensagem,"Edição de aluno",JOptionPane.INFORMATION_MESSAGE);
        }
    }
    public void excluirAgencia(){
        try {
            int linhaSelecionada = tblResultado.getSelectedRow();
            if (linhaSelecionada != -1) {
                Agencia agenciaSelecionada = agencias.get(linhaSelecionada);

                int resposta;
                String mensagem = "Deseja excluir o agencia "
                        + agenciaSelecionada.getNome() + " (Numero Agencia: "
                        + agenciaSelecionada.getNumeroAgencia() + ")?";
                String titulo = "Exclusão de agencia";
                resposta = JOptionPane.showConfirmDialog(this,
                        mensagem, titulo, JOptionPane.YES_NO_OPTION);

                if (resposta == JOptionPane.YES_OPTION) {
                    AgenciaBO agenciaBO = new AgenciaBO();
                    agenciaBO.deletarAgencia(agenciaSelecionada);
                    this.buscarTodasAgencias();
                    this.carregarDadosTabela();
                    String mensagemSuceso = "Agencia "+
                            agenciaSelecionada.getNome()+
                            " Numero Agencia: "
                            + agenciaSelecionada.getNumeroAgencia() + ")"
                            + "excluído com sucesso.";
                    JOptionPane.showMessageDialog(this, 
                            mensagemSuceso, titulo, 
                            JOptionPane.INFORMATION_MESSAGE);
                    
                    
                }else{
                    exibirMensagemSucesso("Dados não excluidos", "Excluir usuário");
                }
            } else {
                String mensagem = "Nenhum cliente selecionado.";
                JOptionPane.showMessageDialog(this,
                        mensagem,
                        "Exclusão de cliente",
                        JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception e) {
            String mensagem = "Erro inesperado! Informe a mensagem de erro ao administrador do sistema.";
            mensagem += "\nMensagem de erro:\n" + e.getMessage();
            JOptionPane.showMessageDialog(this, mensagem, "Exclusão de cliente", JOptionPane.ERROR_MESSAGE);
            this.dispose();
        }
    }
    private void exibirMensagemErro(String mensagem, String titulo) {
        JOptionPane.showMessageDialog(this, mensagem, titulo, JOptionPane.ERROR_MESSAGE);
    }

    private void exibirMensagemSucesso(String mensagem, String titulo) {
        JOptionPane.showMessageDialog(this, mensagem, titulo, JOptionPane.INFORMATION_MESSAGE);
    }
    
