    private class ModeloTabelaAgencia extends AbstractTableModel{

        @Override
        public int getRowCount() {
            return agencias.size();
        }

        @Override
        public int getColumnCount() {
            return 7;
        }

        @Override
        public Object getValueAt(int linha, int coluna) {
            Agencia agencia  = agencias.get(linha);
            if(coluna == 0){
                return agencia.getNome();
            }
            else if(coluna == 1){
                return agencia.getEndereco();
            }else if(coluna == 2){
                return agencia.getCidade();
            }
            else if(coluna == 3){
               return agencia.getEstado();
            }else if(coluna == 4){
                 return agencia.getTelefone();
            }else if(coluna == 5){
                return agencia.getNumeroAgencia();
            } else{
                if(agencia.getPermissao() == 5){
                    return "Usuário Padrão";
                }else{
                    return "Usuário Administrador";
                }
            }
        }
        
        @Override
        public String getColumnName(int coluna){
            if(coluna == 0){
                return "Numero da Agencia:";
            }else if(coluna == 1){
                return "Endereco:";
            }else if(coluna == 2){
                return "Cidade";
            }else if(coluna == 3){
               return "Estado"; 
            }else if(coluna == 4){
                return "Numero da Agencia";
            }else if(coluna == 5){
                return "Telefone";
            }else{
                return "Permissao";
            }
        }
    }
